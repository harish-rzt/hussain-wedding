                scrolled   = _getScrolled(elem.config.container),
                vF         = elem.config.viewFactor,
                // Define the element geometry.
                elemHeight = offset.height,
                elemWidth  = offset.width,
                elemTop    = offset.top,
                elemLeft   = offset.left,
                elemBottom = elemTop  + elemHeight,
